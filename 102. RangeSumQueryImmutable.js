/**
 * @param {number[]} nums
 */
var NumArray = function (nums) {
  this.cumulativeSum = [0]; // Store cumulative sum from index 0 to i
  let sum = 0;

  for (let i = 0; i < nums.length; i++) {
    sum += nums[i];
    this.cumulativeSum.push(sum);
  }
};

/**
 * @param {number} left
 * @param {number} right
 * @return {number}
 */
NumArray.prototype.sumRange = function (left, right) {
  return this.cumulativeSum[right + 1] - this.cumulativeSum[left];
};

